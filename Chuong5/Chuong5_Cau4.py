# Câu 4: Các hàm quan trọng trong xử lý chuỗi của Python

# Một số hàm xử lý chuỗi phổ biến trong Python:

# str.upper() — chuyển chuỗi thành chữ hoa.

# str.lower() — chuyển chuỗi thành chữ thường.

# str.isupper() — kiểm tra ký tự/chữ hoa.

# str.islower() — kiểm tra ký tự/chữ thường.

# str.isdigit() — kiểm tra ký tự/chữ số.

# str.isalpha() — kiểm tra ký tự/chữ cái.

# str.isspace() — kiểm tra ký tự khoảng trắng.

# str.strip() — loại bỏ khoảng trắng đầu và cuối chuỗi.

# str.split(sep=None) — tách chuỗi thành list dựa trên dấu phân cách.

# str.replace(old, new) — thay thế chuỗi con.

# str.capitalize() — viết hoa chữ cái đầu chuỗi.

# str.title() — viết hoa chữ cái đầu mỗi từ.

# str.find(sub) — tìm vị trí chuỗi con, trả về -1 nếu không tìm thấy.

# str.join(iterable) — nối các phần tử trong iterable thành chuỗi.